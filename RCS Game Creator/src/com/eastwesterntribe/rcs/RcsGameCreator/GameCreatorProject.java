package com.eastwesterntribe.rcs.RcsGameCreator;

import java.awt.Color;
import java.awt.Font;
import java.awt.Graphics2D;
import java.awt.Point;
import java.awt.Rectangle;

import com.eastwesterntribe.rcs.RcsGamingLibrary.RcsglStringApplications;
import com.eastwesterntribe.rcs.RcsGamingLibrary.RcsglStringWithCoordinates;


public class GameCreatorProject {
	
	private boolean	hasMaps;	//If it has at least one map
	private RcsglStringWithCoordinates	projectName, noMaps;
	private Font						titleFont, noMapsFont;
	private Color						titleFontColor, noMapsColor;
	
	
	
	/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	//Section 1) Constructor and Init methods
	/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	
	public GameCreatorProject(String projectName, Font titleFont, Color titleFontColor) {
		hasMaps = false;
		this.projectName = new RcsglStringWithCoordinates(projectName);
		this.titleFont = titleFont;
		this.titleFontColor = titleFontColor;
		init();
	}
	
	private void init(){
		this.noMaps = new RcsglStringWithCoordinates("No Maps Found... Start a New One!");
		noMapsFont = titleFont;
		noMapsColor = titleFontColor;
	}
	
	
	/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	//Section 2) Drawing Methods
	/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	
	public void recalibrateStrings(Graphics2D g, RcsglStringApplications stringApplication, Rectangle projectArea, int defaultOffsetX, int defaultOffsetY){
		projectName = stringApplication.positionStringInBottomRightCorner(projectName, g, titleFont, projectArea, defaultOffsetX, defaultOffsetY);
		g.setFont(noMapsFont);
		noMaps.x = stringApplication.centerStringX(noMaps.text, g, projectArea.x + projectArea.width, 0);
		noMaps.y = stringApplication.centerStringY(g, projectArea.y + projectArea.height, 0);
	}
	
	public void draw(Graphics2D g){
		if(hasMaps) {
			
		} else {
			g.setFont(noMapsFont);
			g.setColor(noMapsColor);
			g.drawString(noMaps.text, noMaps.x, noMaps.y);
		}
		
		g.setFont(titleFont);
		g.setColor(titleFontColor);
		g.drawString(projectName.text, projectName.x, projectName.y);
	}
	
	
	/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	//Section 3) Getters and Setters
	/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	
	
	
	public void setProjectName(String newName){
		this.projectName.text = newName;
	}
	
	public void setProjectNameX(int x){
		this.projectName.x = x;
	}
	
	public void setProjectNameY(int y){
		this.projectName.y = y;
	}
	
	public void setProjectNameVariables(RcsglStringWithCoordinates SWC){
		this.projectName = SWC;
	}
	
	public void setProjectNamePosition(Point p){
		setProjectNameX(p.x);
		setProjectNameY(p.y);
	}
	
	public String getProjectName(){
		return projectName.text;
	}
	
	public RcsglStringWithCoordinates getProjectNameVariables(){
		return projectName;
	}
	
	public Point getProjectNameCoords(){
		return new Point(projectName.x, projectName.y);
	}
	
	public boolean hasMaps(){
		return hasMaps;
	}
	
	protected Font getTitleFont(){
		return titleFont;
	}
	
	protected void setTitleFont(Font titleFont){
		this.titleFont = titleFont;
	}
	
	protected Color getTitleFontColor(){
		return titleFontColor;
	}
	
	protected void setTitleFontColor(Color titleFontColor){
		this.titleFontColor = titleFontColor;
	}
	
	
	protected RcsglStringWithCoordinates getNoMaps(){
		return noMaps;
	}
	
	
	protected void setNoMaps(RcsglStringWithCoordinates noMaps){
		this.noMaps = noMaps;
	}
	
	
	protected Font getNoMapsFont(){
		return noMapsFont;
	}
	
	
	protected void setNoMapsFont(Font noMapsFont){
		this.noMapsFont = noMapsFont;
	}
	
	
	protected Color getNoMapsColor(){
		return noMapsColor;
	}
	
	
	protected void setNoMapsColor(Color noMapsColor){
		this.noMapsColor = noMapsColor;
	}
	
	
	
}
